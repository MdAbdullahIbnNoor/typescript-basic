// Basic Data Types

/// Sting
let firstName : string = "Abdullah" //Explicit data type declaring
// let firstName = "Abdullah" //Implicit data type declaring -> Typescript compiler guess the dataTypes by itself

// Number
let roll : number = 180104

// boolean
let isAdmin : boolean = true

// undefined
let x: undefined = undefined

// null
let y: null = null

// Array
let friends : string[] = ['racheal', 'monica'] // For string type array

let rollList : number[] = [10, 15, 25, 36] //for number type array

